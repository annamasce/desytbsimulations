RunAction::RunAction(DetectorConstruction* det,HistoManager* histo, PrimaryGeneratorAction* kin)
:G4UserRunAction(),fDetector(det),fHistoManager(histo), fPrimary(kin), fRun(0)
{ 
  
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

RunAction::~RunAction()
{ 
  delete fHistoManager;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

G4Run* RunAction::GenerateRun()
{ 
  fRun = new Run(fDetector);
  return fRun;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void RunAction::BeginOfRunAction(const G4Run*)
{
 // fHistoManager->Book(); 
  // save Rndm status
  ////  G4RunManager::GetRunManager()->SetRandomNumberStore(true);
  if (isMaster) G4Random::showEngineStatus();
     
  // keep run condition
  if ( fPrimary ) {
    G4ParticleDefinition* particle 
      = fPrimary->GetParticleGun()->GetParticleDefinition();
    G4double energy = fPrimary->GetParticleGun()->GetParticleEnergy();
    G4ThreeVector position = fPrimary->GetParticleGun()->GetParticlePosition();
    fRun->SetPrimary(particle, energy, position);
  }
  
  //histograms
  //        
  G4AnalysisManager* analysisManager = G4AnalysisManager::Instance();
  if ( analysisManager->IsActive() ) {
    analysisManager->OpenFile();
  } 
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void RunAction::EndOfRunAction(const G4Run*)
{ 
//fHistoManager->WriteTuple();
  // print Run summary
  //
  if (isMaster) fRun->EndOfRun();    
  // save histograms
  G4AnalysisManager* analysisManager = G4AnalysisManager::Instance();  
  if ( analysisManager->IsActive() ) {   
    analysisManager->Write();
//    fHistoManager->WriteTuple();
    analysisManager->CloseFile();
  }
  fHistoManager->WriteTuple();
  // show Rndm status
  if (isMaster) G4Random::showEngineStatus();
}

